package mx.carlosespinoza.conversordemoneda.moneda;

/**
 *
 * @author charl
 */
public class tipoDeCambio {
    
        /**
     * Aqui se realiza la declaracion de las constantes de los tipos de cambio
     * Peso Mexicano = mxn;
     * Dolar Estadunidiense = usd;
     * Euro = eur;
     * Yen Japones = jpy;
     * Won Korea del Sur = krw;
     * Libra Esterlina = gbp;
     */
    
    
    private double mxn;
    private double usd;
    private double eur;
    private double jpy;
    private double krw;
    private double gbp;
    /**
     * @return the mxn
     */
    public double getMxn() {
        return mxn;
    }

    /**
     * @param mxn the mxn to set
     */
    public void setMxn(double mxn) {
        this.mxn = mxn;
    }

    /**
     * @return the usd
     */
    public double getUsd() {
        return usd;
    }

    /**
     * @param usd the usd to set
     */
    public void setUsd(double usd) {
        this.usd = usd;
    }

    /**
     * @return the eur
     */
    public double getEur() {
        return eur;
    }

    /**
     * @param eur the eur to set
     */
    public void setEur(double eur) {
        this.eur = eur;
    }

    /**
     * @return the jpy
     */
    public double getJpy() {
        return jpy;
    }

    /**
     * @param jpy the jpy to set
     */
    public void setJpy(double jpy) {
        this.jpy = jpy;
    }

    /**
     * @return the krw
     */
    public double getKrw() {
        return krw;
    }

    /**
     * @param krw the krw to set
     */
    public void setKrw(double krw) {
        this.krw = krw;
    }

    /**
     * @return the gbp
     */
    public double getGbp() {
        return gbp;
    }

    /**
     * @param gbp the gbp to set
     */
    public void setGbp(double gbp) {
        this.gbp = gbp;
    }

    
    
    
}
